    public final void setJob( ResizeJob job ) {
        this.job = job;
        if( job.outputDimensions.size() > 0 ) {
            for( OutputDimension dim : job.outputDimensions) {
                outputTabs.add( "Resolution " + (outputTabs.getTabCount() + 1), new OutputFormatPanel( dim ) );
                outputTabs.setSelectedIndex( outputTabs.getTabCount() - 1 );
            }
        } else {
            addResBtnActionPerformed(null);
        }
    }

