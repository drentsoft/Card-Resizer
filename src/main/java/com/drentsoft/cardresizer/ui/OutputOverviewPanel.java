    private void addResBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addResBtnActionPerformed
        OutputDimension dim = new OutputDimension();
        job.outputDimensions.add( dim );
        outputTabs.add( "Resolution " + (outputTabs.getTabCount() + 1), new OutputFormatPanel(  dim ) );
        outputTabs.setSelectedIndex( outputTabs.getTabCount() - 1 );
    }//GEN-LAST:event_addResBtnActionPerformed

    private void copyResBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_copyResBtnActionPerformed
        OutputDimension origDim = job.outputDimensions.get( outputTabs.getSelectedIndex() );
        OutputDimension newDim = new OutputDimension(origDim);
        job.outputDimensions.add( newDim );
        outputTabs.add( "Resolution " + (outputTabs.getTabCount() + 1), new OutputFormatPanel( newDim ) );
        outputTabs.setSelectedIndex( outputTabs.getTabCount() - 1 );
    }//GEN-LAST:event_copyResBtnActionPerformed

    private void delResBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delResBtnActionPerformed
        if( job.outputDimensions.size() > 1 ) {
            int rv = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete resolution output module?", "Delete Resolution?", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE );
            if( rv == JOptionPane.OK_OPTION ) {
                job.outputDimensions.remove( outputTabs.getSelectedIndex() );
                outputTabs.remove( outputTabs.getSelectedIndex() );
            }
        }
    }//GEN-LAST:event_delResBtnActionPerformed


